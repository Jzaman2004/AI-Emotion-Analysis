def clopper_pearson(k, n, alpha=0.05):
    """Exact binomial CI. Returns (lower, upper)."""
    if n == 0:
        return (0.0, 1.0)
    lo = stats.beta.ppf(alpha / 2,     k,     n - k + 1) if k > 0 else 0.0
    hi = stats.beta.ppf(1 - alpha / 2, k + 1, n - k)     if k < n else 1.0
    return (lo, hi)

